export const getRandomReaction = (reactions) => {
    return reactions[Math.floor(Math.random() * reactions.length)];
};

export const getQuestionIntro = (chapter, questionIndex) => {
    const key = `chapter${chapter}`;
    const intros = QUIZ_REACTIONS.questionIntros[key];
    if (!intros) return null;
    return intros[Math.min(questionIndex, intros.length - 1)];
};

export const getEndingType = (bossesDefeated, totalScore, maxScore) => {
    const scoreRatio = totalScore / maxScore;
    if (bossesDefeated === 3 && scoreRatio >= 0.7) return 'perfect';
    if (bossesDefeated >= 2) return 'good';
    return 'partial';
};
