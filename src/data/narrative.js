/**
 * 配列からランダムに1つ選択する
 * @param {Array} reactions - リアクションの配列
 * @returns {Object} 選択されたリアクション
 */
export const getRandomReaction = (reactions) => {
    return reactions[Math.floor(Math.random() * reactions.length)];
};

/**
 * Stage1の問題インデックスに応じたつなぎセリフを取得する
 * @param {number} questionIndex - 0-based の問題インデックス
 * @returns {Object} 対応するつなぎセリフ
 */
export const getQuestionIntro = (questionIndex) => {
    const intros = STAGE1_NARRATIVE.questionIntros;
    return intros[Math.min(questionIndex, intros.length - 1)];
};

/**
 * Stage1の正答率に応じたStage2Introのコメントを取得する
 * @param {number} accuracy - 正答率（0-100）
 * @returns {Object} 対応するコメント
 */
export const getResultComment = (accuracy) => {
    if (accuracy >= 80) return STAGE2INTRO_NARRATIVE.resultComment.excellent;
    if (accuracy >= 60) return STAGE2INTRO_NARRATIVE.resultComment.good;
    return STAGE2INTRO_NARRATIVE.resultComment.needsWork;
};

/**
 * 任意のnarrativeオブジェクトから正答率に応じたコメントを取得する
 * @param {number} accuracy - 正答率（0-100）
 * @param {Object} narrativeObj - resultComment.excellent/good/needsWork を持つオブジェクト
 * @returns {Object} 対応するコメント
 */
export const getResultCommentFromNarrative = (accuracy, narrativeObj) => {
    if (accuracy >= 80) return narrativeObj.resultComment.excellent;
    if (accuracy >= 60) return narrativeObj.resultComment.good;
    return narrativeObj.resultComment.needsWork;
};

